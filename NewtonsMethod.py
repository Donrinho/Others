#牛顿迭代法求平方根
'''
牛顿迭代法：求a的平方根
令f(x) = x^2-a，目标：f(x)=0
f(x)上任意一点的切线斜率为2x
假设点(x0, y0)处的切线方程是y=kx+b，则切线与x轴的交点横坐标为-b/k
b = y0 - k*x0
k = 2 * x0
y0 = x0 ^ 2 - a
求得过点(x0, y0)的切线与x轴交点的横坐标：-b/k = (x0 + a / x0) / 2
记x1 = -b/k = (x0 + a / x0) / 2
x1比x0更靠近函数f(x)与x轴交点的横坐标（即a的正平方根）
不断迭代，知道xn的平方与a的误差小于设定误差值
'''
def newtonsMethod(a):
    err = 1e-15
    x = a
    while(x**2 - a > err):
        x = 0.5 * (x + a/x)
    return x

